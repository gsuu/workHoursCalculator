import test from "node:test";
import assert from "node:assert/strict";

import { validateDetailRows } from "./monthlyAttendanceImport.js";

test("근무결과 상세는 안내 시트와 데이터 시트가 분리돼도 정상 검증된다", () => {
  const guideRows = [
    ["(안내)"],
    ["- 소정근무: 일일근무규칙에서 정해진 일 소정근무시간을 기준으로 한 소정근무시간"],
    ["- 연장근무: 정해진 소정근무시간을 초과하는 근무시간"],
    ["- 휴일근무(150%): 근로형태에서 휴일로 설정된 날 근무한 총 시간"],
    ["- 휴일근무(200%): 근로형태에서 휴일로 설정된 날 근무한 것으로 소정근무시간을 초과한 연장시간(휴일연장근무)"],
    ["- 야간근무: 오후 10시부터 다음 날 오전 6시 사이에 근무한 시간"]
  ];

  const dataRows = [
    ["이름", "홍길동", "", "", "", ""],
    ["사번", "CT1001", "", "", "", ""],
    ["소속", "CTTD", "", "", "", ""],
    ["날짜", "규칙", "소정", "연장", "야간", "휴일"],
    ["1(일)", "휴일", "00:00:00", "00:00:00", "00:00:00", "00:00:00"],
    ["2(월)", "9시출근", "08:00:00", "00:00:00", "00:00:00", "00:00:00"]
  ];

  assert.doesNotThrow(() => {
    validateDetailRows(dataRows, { year: 2026, month: 3 }, guideRows);
  });
});
